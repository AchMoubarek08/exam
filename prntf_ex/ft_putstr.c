#include "ft_printf.h"

void	ft_putstr(char *s, int *count)
{
	int	i;

	i = 0;
	if (s)
	{
		while (s[i])
		{
			write(1, &s[i], 1);
			i++;
			*count += 1;
		}
	}
	else
	{
		write(1, "(null)", 6);
		*count += 6;
	}
}
